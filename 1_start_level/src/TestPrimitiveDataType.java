/**
 * 测试基本数据类型（整型变量）
 */
public class TestPrimitiveDataType {
    public static void main(String[] args) {
        int a =15;  //int型
        int b = 0b10111; //二进制
        int c = 0135;//八进制
        int d = 0xa4;//十六进制
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
